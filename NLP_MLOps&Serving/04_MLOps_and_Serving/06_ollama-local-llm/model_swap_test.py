# 📚 파일명: model_swap_test.py
# 📌 목적: Ollama의 "자동 모델 교체(Hot-Swapping)" 기능을 테스트하는 스크립트
# =============================================================================
#
# ┌─────────────────────────────────────────────────────────────────────────────┐
# │  🎯 이 파일에서 배우는 것                                                    │
# ├─────────────────────────────────────────────────────────────────────────────┤
# │  1. "모델 스와핑(Model Swapping)"의 개념                                       │
# │     - 하나의 서버가 여러 개의 AI 모델을 번갈아가며 서빙하는 것                      │
# │     - 사용자가 직접 모델을 끄고/켜지 않아도 Ollama가 알아서 관리                    │
# │                                                                             │
# │  2. "메모리(Memory) 관리"의 중요성                                           │
# │     - AI 모델은 메모리(RAM/VRAM)를 많이 차지함 (예: 9GB, 14GB...)           │
# │     - 그래픽카드 메모리가 부족하면 이전 모델을 빼고 새 모델을 올려야 함             │
# │     - 이 과정에서 "로딩 시간"이 발생 → 지연 시간(Latency) 증가              │
# │                                                                             │
# │  3. "콜드 스타트(Cold Start) vs 핫 스타트(Hot Start)"                        │
# │     - Cold Start: 모델이 메모리에 없어서 새로 올려야 함 (느림, 수십 초)      │
# │     - Hot Start: 모델이 이미 메모리에 있음 (빠름, 1초 미만)                  │
# │                                                                             │
# │  4. 파이썬 기초: 함수(def) 정의와 호출, 리스트 순회                         │
# └─────────────────────────────────────────────────────────────────────────────┘
#
# ┌─────────────────────────────────────────────────────────────────────────────┐
# │  🤔 왜 이걸 배우나요?                                                        │
# ├─────────────────────────────────────────────────────────────────────────────┤
# │  - 실제 서비스에서는 상황에 따라 다른 모델을 써야 할 때가 있음               │
# │    예) 간단한 질문 → 작은 모델 (빠름), 복잡한 질문 → 큰 모델 (정확함)       │
# │  - 이런 상황에서 Ollama가 어떻게 동작하는지 이해해야 효율적인 설계 가능     │
# │  - 하드웨어(GPU 메모리) 제한을 이해하고 적절한 모델 조합을 선택하는 기초    │
# └─────────────────────────────────────────────────────────────────────────────┘
#
# ┌─────────────────────────────────────────────────────────────────────────────┐
# │  💻 현재 설치된 모델들 (참고용)                                              │
# ├─────────────────────────────────────────────────────────────────────────────┤
# │  NAME              SIZE      설명                                           │
# │  qwen2.5:14b       9.0 GB    중국 알리바바의 대형 언어 모델                  │
# │  deepseek-r1:14b   9.0 GB    중국 딥시크의 추론 특화 모델                    │
# │  gemma2:9b         5.4 GB    구글의 중형 언어 모델                           │
# │  ─────────────────────────────────────────────────────────────────────────  │
# │  합계: 약 23.4 GB → 대부분의 GPU에서 동시 로드 불가능!                       │
# └─────────────────────────────────────────────────────────────────────────────┘

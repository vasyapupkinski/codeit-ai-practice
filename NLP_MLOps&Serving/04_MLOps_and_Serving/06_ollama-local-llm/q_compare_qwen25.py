    # -------------------------------------------------------------------------
    # [6-1] 각 모델별 벤치마크 실행
    # -------------------------------------------------------------------------
    for m in MODELS:
        avg, p95, _ = benchmark(m, warmup=2, n=10)
        # 📌 _, = ...:
        #    세 번째 반환값(텍스트)은 여기서 안 쓰므로 _로 받음
        results[m] = (avg, p95)
        # 결과를 딕셔너리에 저장

    # -------------------------------------------------------------------------
    # [6-2] 최종 비교 결과 출력
    # -------------------------------------------------------------------------
    print("=" * 80)
    print("---- Comparison Result ----")
    print("=" * 80)

    for m, (avg, p95) in results.items():
        # 📌 dict.items(): (키, 값) 쌍을 순회
        # 📌 (avg, p95): 값 튜플을 다시 언패킹
        print(f"- {m} | AVG={avg:.2f}s | P95={p95:.2f}s")



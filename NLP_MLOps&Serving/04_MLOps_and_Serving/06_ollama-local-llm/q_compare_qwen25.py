def call_model(model: str) -> tuple[float, str]:
    # 📌 함수 정의 분석:
    #    def call_model(model: str) -> tuple[float, str]:
    #    │           │      │   │      └─ 반환 타입: (숫자, 문자열) 쌍
    #    │           │      │   └─ 타입 힌트: model은 문자열(str)
    #    │           │      └─ 매개변수 이름
    #    │           └─ 함수 이름
    #    └─ 함수 정의 키워드
    #
    # 📌 타입 힌트 (Type Hint):
    #    - 파이썬 3.5+에서 추가된 기능
    #    - 실행에는 영향 없지만, 코드 가독성과 자동완성(IDE)에 도움
    #
    # 📌 tuple[float, str]:
    #    - 튜플 = 변경 불가능한 순서있는 묶음
    #    - 이 함수는 (소요시간, 응답텍스트) 두 값을 한 번에 반환
    
    """Ollama API 호출 + latency 측정"""
    # 📌 Docstring (문서화 문자열):
    #    함수 바로 아래에 """ """로 작성하면 함수 설명으로 인식됨
    #    help(call_model) 하면 이 설명이 출력됨

    payload = {
        "model": model,
        # 어떤 모델을 사용할지 (함수 호출 시 전달받은 값)
        
        "prompt": PROMPT,
        # 위에서 정의한 테스트용 질문
        
        "stream": False,
        # 스트리밍 비활성화 (한 번에 전체 응답 받기)
        
        "options": {
            "num_predict": 300,   
            # 출력 길이를 300 토큰으로 고정
            # 왜? 모든 모델이 비슷한 길이의 답변을 생성하도록
            # 그래야 "속도" 비교가 공정해짐
            
            "temperature": 0.0    
            # 온도 = AI의 창의성/무작위성 정도
            # 0.0 = 항상 가장 확률 높은 답변만 선택 (결정적)
            # 1.0 = 다양한 답변 가능 (창의적)
            # 왜 0.0? 매번 같은 답이 나와야 공정한 비교 가능
        }
    }

    start = time.time()
    # 시작 시각 기록
    
    r = requests.post(URL, json=payload, timeout=600)
    # Ollama 서버에 POST 요청
    # timeout=600: 최대 10분까지 응답 대기 (긴 답변 대비)
    
    r.raise_for_status()
    # 📌 raise_for_status():
    #    HTTP 상태 코드가 200이 아니면 에러 발생시킴
    #    예: 404, 500 등의 에러를 자동으로 감지
    
    elapsed = time.time() - start
    # 소요 시간 = 현재 시각 - 시작 시각

    text = r.json().get("response", "")
    # 📌 .get("response", ""):
    #    딕셔너리에서 "response" 키의 값을 가져옴
    #    만약 키가 없으면 빈 문자열 "" 반환 (에러 방지)
    #    cf) r.json()["response"]는 키가 없으면 에러 발생
    
    return elapsed, text
    # 📌 return 값1, 값2:
    #    두 값을 튜플로 묶어서 반환
    #    호출하는 쪽에서 t, text = call_model(...) 으로 받을 수 있음


# -----------------------------------------------------------------------------
# [4단계] P95 계산 함수 정의
# -----------------------------------------------------------------------------

def approx_p95(times: list[float]) -> float:
    # 📌 list[float]: float(실수) 값들의 리스트를 받겠다는 타입 힌트
    
    """측정된 latency 중 '느린 쪽 95%' 지점(P95)을 간단히 계산"""
    #
    # 📌 P95 (95th Percentile)이란?
    #    - 측정값들을 오름차순 정렬했을 때 95% 지점의 값
    #    - 예: [1, 2, 3, ..., 100] 중 95번째 값
    #
    # 📌 왜 평균 말고 P95를 쓰나요?
    #    - 평균은 극단적인 값(이상치)에 영향을 많이 받음
    #    - P95는 "대부분의 사용자(95%)가 경험하는 최대 지연"을 나타냄
    #    - 실제 서비스 SLA(서비스 수준 협약)에서 자주 사용됨
    #      예: "API 응답의 P95가 3초 이내여야 한다"

    times_sorted = sorted(times)
    # 📌 sorted(리스트): 오름차순 정렬된 새 리스트 반환
    #    예: sorted([3, 1, 2]) → [1, 2, 3]
    #    원본 리스트는 변하지 않음
    
    idx = int(0.95 * (len(times_sorted) - 1))
    # 95% 위치의 인덱스 계산
    # 예: 10개 측정 시 → 0.95 * 9 = 8.55 → int() → 8번 인덱스
    # 
    # 📌 int(): 소수점 버림 (정수로 변환)
    #    int(8.55) → 8
    #
    # 📌 len(리스트) - 1: 마지막 인덱스
    #    리스트 인덱스는 0부터 시작하므로
    
    return times_sorted[idx]
    # 정렬된 리스트에서 95% 위치의 값 반환


# -----------------------------------------------------------------------------
# [5단계] 벤치마크 함수 정의
# -----------------------------------------------------------------------------

def benchmark(model: str, warmup: int = 2, n: int = 10) -> tuple[float, float, str]:
    # 📌 매개변수 기본값:
    #    warmup: int = 2  → 기본값이 2, 호출 시 생략 가능
    #    n: int = 10      → 기본값이 10
    #    예: benchmark("model") → warmup=2, n=10 사용
    #        benchmark("model", warmup=3) → warmup=3, n=10 사용
    
    """단일 모델 벤치마크"""

    # -------------------------------------------------------------------------
    # [5-1] Warm-up (워밍업): 측정에서 제외
    # -------------------------------------------------------------------------
    for _ in range(warmup):
        # 📌 _ (언더스코어):
        #    "이 변수는 안 쓸 거야"라는 관례적 표현
        #    반복만 하고 인덱스 값은 필요 없을 때 사용
        call_model(model)
    # 
    # 📌 왜 워밍업을 하나요?
    #    - 첫 번째 호출은 모델 로딩 시간이 포함됨 (콜드 스타트)
    #    - 이건 "모델 자체의 성능"이 아니라 "시스템 오버헤드"
    #    - 공정한 비교를 위해 처음 몇 번은 버림 (측정 안 함)

    times: list[float] = []
    # 측정된 시간들을 저장할 빈 리스트
    # 📌 타입 힌트: list[float] = float들의 리스트
    
    last_text = ""
    # 마지막 응답 텍스트 저장용 (나중에 샘플로 출력)

    print("=" * 80)
    # "=" 문자를 80번 반복 출력 (구분선)
    print(f"MODEL: {model}")
    print("=" * 80)

    # -------------------------------------------------------------------------
    # [5-2] 본 측정: n번 반복
    # -------------------------------------------------------------------------
    for i in range(n):
        t, text = call_model(model)
        # 📌 t, text = ...:
        #    함수가 반환한 튜플을 두 변수에 각각 할당 (언패킹)
        #    t = 소요시간, text = 응답 텍스트
        
        times.append(t)
        # 측정된 시간을 리스트에 추가
        
        last_text = text
        # 마지막 응답 저장 (샘플 출력용)
        
        print(f"[{i+1}/{n}] latency={t:.2f}s")
        # 진행 상황 실시간 출력

    # -------------------------------------------------------------------------
    # [5-3] 통계 계산 및 결과 출력
    # -------------------------------------------------------------------------
    avg = statistics.mean(times)
    # 📌 statistics.mean(리스트): 평균 계산
    #    예: mean([1, 2, 3]) → 2.0
    
    p95 = approx_p95(times)
    # 위에서 정의한 P95 계산 함수 호출

    print("\n--- Summary ---")
    print(f"AVG latency: {avg:.2f}s")
    # 평균 소요 시간
    print(f"P95 latency: {p95:.2f}s")
    # 95번째 백분위 소요 시간

    print("\n--- Sample Response (first 800 chars) ---")
    print(last_text.strip()[:800])
    # 📌 .strip(): 앞뒤 공백/줄바꿈 제거
    # 📌 [:800]: 처음 800글자만 (너무 길면 잘라서 보여줌)
    print()

    return avg, p95, last_text
    # 평균, P95, 마지막 응답 텍스트를 반환



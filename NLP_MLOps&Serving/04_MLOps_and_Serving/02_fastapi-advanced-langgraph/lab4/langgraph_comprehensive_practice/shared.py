# ===============================================
# 📍 [구역 3] 공용 도구 (Tools) 정의
# ===============================================
#
# [핵심 개념] 도구(Tool)란?
# - AI가 "혼자서는 못하는 일"을 대신 해주는 외부 기능입니다.
# - 예: 실시간 검색, 수학 계산, 데이터베이스 조회, API 호출 등
#
# [Q&A] 왜 AI가 직접 계산을 안 하나요?
# - GPT는 "언어 모델"이라서 숫자 계산에 약합니다.
# - "1234 * 5678"을 물어보면 가끔 틀립니다!
# - 그래서 계산은 파이썬 eval()에게 맡기고, 결과만 받아서 사용합니다.
#
# [이 파일에서 정의하는 도구 2가지]
# 1. calculator_tool: 수식 문자열을 받아서 계산
# 2. knowledge_search_tool: 키워드로 간단한 지식 검색
# ===============================================

def calculator_tool(expression: str) -> str:
    """
    [도구 1] 안전한 계산기
    
    ┌─────────────────────────────────────────────────────────────────┐
    │ [역할] 수식 문자열을 받아서 계산 결과를 반환합니다.             │
    │ [예시] "1 + 2 * 3" -> "계산 결과: 7"                           │
    │ [주의] eval()을 쓰지만, 위험한 코드는 필터링합니다.             │
    └─────────────────────────────────────────────────────────────────┘
    
    
    1. expression 문자열을 받습니다.
    2. 위험한 키워드(import, exec 등)가 있는지 검사합니다.
    3. 안전하면 eval()로 계산합니다.
    4. 결과를 문자열로 반환합니다.
    
    [Q&A] eval()이 위험하다던데요?
    - 맞습니다! eval("import os; os.system('rm -rf /')")처럼 악성 코드 실행이 가능합니다.
    - 그래서 아래에서 위험한 키워드를 필터링하고, __builtins__도 비워둡니다.
    - 실무에서는 numexpr이나 sympy 같은 안전한 라이브러리를 씁니다.
    
    Args:
        expression (str): 계산할 수식 문자열 (예: "15 * 23")
        
    Returns:
        str: 계산 결과 문자열 (예: "계산 결과: 345")
    """
    try:
        # [Step 1] 공백 제거 (양쪽 끝)
        expression = expression.strip()
        
        # [Step 2] 보안 검사: 위험한 키워드 필터링
        # - 이 단어들이 포함되어 있으면 해킹 시도로 간주합니다.
        # - import: 외부 모듈 불러오기 (os, sys 등)
        # - __: 파이썬 내부 속성 접근 (__class__, __import__ 등)
        # - exec, eval: 코드 동적 실행
        # - open: 파일 열기 (시스템 파일 읽기 가능)
        dangerous_keywords = ['import', '__', 'exec', 'eval', 'open']
        
        # any(): 하나라도 True면 True를 반환합니다.
        # d in expression.lower(): 키워드가 수식에 포함되어 있는지 확인
        if any(d in expression.lower() for d in dangerous_keywords):
            return "보안 경고: 허용되지 않는 표현식입니다."
            
        # [Step 3] 제한된 환경에서 계산 실행
        # - {"__builtins__": {}}: 파이썬 내장 함수(print, open 등)를 모두 비활성화
        # - {}: 지역 변수도 비워둠
        # - 이렇게 하면 순수하게 수학 연산만 가능합니다.
        result = eval(expression, {"__builtins__": {}}, {})
        
        return f"계산 결과: {result}"
        
    except Exception as e:
        # [에러 처리] 수식이 잘못되었을 때 (예: "1 + + 2")
        return f"계산 오류: {str(e)}"


def knowledge_search_tool(query: str) -> str:
    """
    [도구 2] 지식 검색 (간단한 키워드 매칭)
    
    ┌─────────────────────────────────────────────────────────────────┐
    │ [역할] 질문에서 키워드를 찾아 미리 저장된 지식을 반환합니다.    │
    │ [실무 대체] 구글 검색 API, RAG(벡터 DB), 위키피디아 API 등      │
    └─────────────────────────────────────────────────────────────────┘
    
    
    1. query 문자열을 받습니다.
    2. knowledge_base 딕셔너리에서 매칭되는 키워드를 찾습니다.
    3. 찾으면 해당 정보를 반환, 못 찾으면 "검색 결과 없음"을 반환합니다.
    
    [Q&A] 실무에서는 어떻게 하나요?
    - 이 코드는 "3개 키워드만 아는" 초간단 버전입니다.
    - 실무에서는:
      1. Google Custom Search API: 실제 구글 검색 결과를 가져옵니다.
      2. RAG(Retrieval-Augmented Generation): 벡터 DB에서 관련 문서를 찾습니다.
      3. LangChain Tools: serpapi, wikipedia 같은 도구를 연결합니다.
    
    Args:
        query (str): 검색할 질문 (예: "FastAPI가 뭐야?")
        
    Returns:
        str: 검색 결과 문자열
    """
    # [Step 1] 지식 베이스 정의
    # - 딕셔너리 형태로 키워드와 설명을 저장합니다.
    # - 실무에서는 DB나 벡터 스토어에서 가져옵니다.
    knowledge_base = {
        "fastapi": "FastAPI는 Python의 고성능 비동기 웹 프레임워크입니다. 타입 힌트 기반으로 자동 문서화(Swagger)를 제공합니다.",
        "langgraph": "LangGraph는 LangChain 팀이 만든 상태 기반 에이전트 워크플로우 도구입니다. 복잡한 AI 흐름을 그래프로 설계할 수 있습니다.",
        "python": "Python은 읽기 쉽고 강력한 프로그래밍 언어입니다. 웹, AI, 데이터 분석 등 다양한 분야에서 사용됩니다."
    }
    
    # [Step 2] 키워드 매칭 검색
    # - 대소문자를 무시하고 검색합니다 (.lower())
    for key, value in knowledge_base.items():
        if key.lower() in query.lower():
            return f"검색 결과: {value}"
            
    # [Step 3] 매칭되는 키워드가 없으면
    return "검색 결과 없음: 관련 정보를 찾을 수 없습니다."



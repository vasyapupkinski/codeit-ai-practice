#
# ┌─────────────────────────────────────────────────────────────────────────────┐
# │  🎯 이 파일에서 배우는 것                                                    │
# ├─────────────────────────────────────────────────────────────────────────────┤
# │  1. "지연 시간(Latency)"의 중요성                                            │
# │     - 사용자가 질문하고 답변을 받기까지 기다리는 시간                        │
# │     - 1초 vs 10초 → 사용자 경험(UX)에 엄청난 차이                           │
# │                                                                             │
# │  2. "반복 측정"의 필요성                                                     │
# │     - 한 번만 측정하면 우연히 빠르거나 느릴 수 있음                          │
# │     - 여러 번 측정해서 평균을 내야 신뢰할 수 있는 데이터가 됨               │
# │                                                                             │
# │  3. "첫 번째 호출 vs 이후 호출" 차이 관찰                                    │
# │     - 첫 호출: 모델을 메모리에 올리는 시간이 포함됨 (느림)                   │
# │     - 이후 호출: 모델이 이미 메모리에 있어서 빠름                            │
# │     - 이 차이를 "콜드 스타트(Cold Start)"라고 부름                          │
# │                                                                             │
# │  4. 파이썬 기초: for 반복문, 리스트, 평균 계산                              │
# └─────────────────────────────────────────────────────────────────────────────┘
#
# ┌─────────────────────────────────────────────────────────────────────────────┐
# │  🤔 왜 이걸 배우나요?                                                        │
# ├─────────────────────────────────────────────────────────────────────────────┤
# │  - 실제 서비스에서는 "느린 AI"보다 "빠른 AI"가 훨씬 가치 있음               │
# │  - 양자화(Quantization)를 적용하면 속도가 빨라지는데, 그 효과를 측정해야 함 │
# │  - 다양한 모델(Llama, Gemma, Qwen 등)의 속도를 비교하는 기초가 됨           │
# └─────────────────────────────────────────────────────────────────────────────┘
#
# =============================================================================
# 🔧 코드 시작
# =============================================================================

# -----------------------------------------------------------------------------
# [1단계] 라이브러리 불러오기
# -----------------------------------------------------------------------------

import requests  
# requests: 인터넷으로 데이터를 주고받는 라이브러리
# 이 코드에서는 Ollama 서버에 질문을 보내고 답변을 받는 데 사용

import time      
# time: 시간 관련 기능을 제공하는 라이브러리
# time.time()으로 현재 시각을 가져와서 실행 시간을 측정


# -----------------------------------------------------------------------------
# [2단계] API 주소와 요청 데이터 설정
# -----------------------------------------------------------------------------

url = "http://localhost:11434/api/generate"
# Ollama 서버의 "텍스트 생성" API 주소
# ollama_client.py에서 설명했던 것과 동일

payload = {
    "model": "gemma2:9b",
    # 테스트할 모델 이름
    # 💡 다른 모델로 바꿔서 속도 비교 가능 (예: "qwen2.5:14b")
    
    "prompt": "FastAPI가 뭔지 한 문장으로.",
    # 테스트용 질문
    # 짧은 질문 = 답변도 짧음 = 순수하게 "모델 처리 속도"를 측정하기 좋음
    
    "stream": False
    # 답변을 한 번에 받음 (스트리밍 X)
    # 시간 측정을 깔끔하게 하려면 False가 좋음
}


# -----------------------------------------------------------------------------
# [3단계] 결과를 저장할 리스트 준비
# -----------------------------------------------------------------------------

times = []
# times = 빈 리스트(list)를 생성
# 
# 📌 리스트(List)란?
#    - 여러 개의 값을 순서대로 담는 상자
#    - 대괄호 []로 만듦
#    - 예: [1.5, 2.3, 1.8] → 3개의 숫자가 담긴 리스트
#
# 💡 이 리스트에 각 반복에서 측정한 시간을 하나씩 추가할 예정

print("=== Latency Test Starting (5 iterations) ===")
# 테스트 시작을 알리는 메시지 출력


# -----------------------------------------------------------------------------
# [4단계] 에러 처리와 반복 측정
# -----------------------------------------------------------------------------

try:
    # try: 에러가 발생할 수 있는 코드를 감싸는 블록
    # 네트워크 오류나 서버 문제가 생겨도 프로그램이 갑자기 멈추지 않게 함
    
    # -------------------------------------------------------------------------
    # [4-1] for 반복문: 5번 반복 실행
    # -------------------------------------------------------------------------
    for i in range(5):
        # for i in range(5): 
        #   - range(5) = [0, 1, 2, 3, 4] 와 같은 숫자 목록을 생성
        #   - i 변수가 0부터 4까지 차례로 바뀌면서 아래 코드를 5번 실행
        #
        # 📌 range() 함수:
        #    range(5)     → 0, 1, 2, 3, 4 (5개)
        #    range(1, 6)  → 1, 2, 3, 4, 5 (5개)
        #    range(0, 10, 2) → 0, 2, 4, 6, 8 (2씩 증가)
        
        # ---------------------------------------------------------------------
        # [4-2] 시간 측정 시작
        # ---------------------------------------------------------------------
        start = time.time()
        # 현재 시각을 초 단위로 저장
        
        # ---------------------------------------------------------------------
        # [4-3] Ollama 서버에 요청 보내기
        # ---------------------------------------------------------------------
        res = requests.post(url, json=payload)
        # POST 방식으로 Ollama에 질문 전송
        # 답변이 올 때까지 이 줄에서 기다림 (동기적 실행)
        
        # ---------------------------------------------------------------------
        # [4-4] 시간 측정 종료
        # ---------------------------------------------------------------------
        elapsed = time.time() - start
        # 끝난 시각 - 시작 시각 = 소요 시간
        
        # ---------------------------------------------------------------------
        # [4-5] 응답 확인 및 에러 처리
        # ---------------------------------------------------------------------
        data = res.json()
        # 서버 응답을 JSON → 딕셔너리로 변환
        
        if "error" in data:
            # "error" 키가 있으면 = 뭔가 문제가 생김
            print(f"!!! Error at iteration {i+1}: {data['error']}")
            # i+1을 출력하는 이유: i는 0부터 시작하므로 사람이 보기엔 1부터 시작하는 게 자연스러움
            break
            # break: 현재 반복문(for)을 즉시 탈출
            # 에러가 나면 더 이상 테스트할 필요 없으므로 중단
        
        # ---------------------------------------------------------------------
        # [4-6] 성공 시 결과 저장 및 출력
        # ---------------------------------------------------------------------
        times.append(elapsed)
        # times 리스트에 이번에 측정한 시간을 추가
        #
        # 📌 .append() 메서드:
        #    리스트의 맨 끝에 새 값을 추가하는 기능
        #    예: times = [1.5]  →  times.append(2.3)  →  times = [1.5, 2.3]
        
        print(f"[{i+1}/5] latency={elapsed:.2f}s")
        # 현재 진행 상황과 소요 시간 출력
        # {i+1}/5: "1/5", "2/5", ... 형태로 진행률 표시
        # {elapsed:.2f}: 소수점 2자리까지 표시 (예: 3.14)
        # 💡 이 print가 for 안에 들여쓰기 되어 있어서 매 반복마다 실행됨!
    
    # -------------------------------------------------------------------------
    # [4-7] 모든 반복 완료 후 평균 계산
    # -------------------------------------------------------------------------
    if times:
        # if times: = "times 리스트에 뭔가 들어있다면"
        # 빈 리스트 [] 는 False로 취급되고, 값이 있으면 True
        
        print("\n=== Summary ===")
        # \n = 줄바꿈 (출력을 보기 좋게 만들기 위해)
        
        print(f"AVG latency: {sum(times)/len(times):.2f}s")
        # 📌 sum(times): 리스트 안 모든 숫자의 합
        #    예: sum([1.5, 2.3, 1.8]) = 5.6
        #
        # 📌 len(times): 리스트의 길이 (= 요소 개수)
        #    예: len([1.5, 2.3, 1.8]) = 3
        #
        # 📌 평균(Average) = 합계 / 개수
        #    예: 5.6 / 3 = 1.87초

except Exception as e:
    # except: try 블록 안에서 에러가 발생하면 여기로 옴
    print(f"!!! Connection Error: {e}")
    # 에러 내용을 출력 (예: "Connection refused" = 서버가 꺼져있음)



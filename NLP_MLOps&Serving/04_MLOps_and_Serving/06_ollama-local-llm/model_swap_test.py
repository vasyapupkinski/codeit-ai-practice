# -----------------------------------------------------------------------------
# [1단계] 라이브러리 불러오기
# -----------------------------------------------------------------------------

import requests  
# requests: HTTP 요청을 보내는 라이브러리
# Ollama 서버와 통신하는 데 사용

import time      
# time: 시간 측정에 사용


# -----------------------------------------------------------------------------
# [2단계] 기본 설정
# -----------------------------------------------------------------------------

url = "http://localhost:11434/api/generate"
# Ollama API 주소 (텍스트 생성 엔드포인트)

models = ["qwen2.5:14b", "deepseek-r1:14b", "gemma2:9b"]
# 📌 테스트할 모델 목록을 리스트로 정의
# 
# 💡 리스트(List):
#    - 여러 값을 순서대로 담는 자료구조
#    - 대괄호 [] 안에 쉼표로 구분된 값들
#    - models[0] = "qwen2.5:14b" (첫 번째 요소, 인덱스는 0부터 시작)
#    - models[1] = "deepseek-r1:14b"
#    - models[2] = "gemma2:9b"
#
# 🔧 자신이 설치한 모델에 맞게 수정하세요!
#    확인 방법: 터미널에서 `ollama list` 실행


# -----------------------------------------------------------------------------
# [3단계] 모델 테스트 함수(Function) 정의
# -----------------------------------------------------------------------------

def test_model_swap(model_name):
    # 📌 def 키워드: 함수를 정의할 때 사용
    #
    # 함수(Function)란?
    #   - 특정 작업을 수행하는 코드 묶음
    #   - 한 번 만들어두면 여러 곳에서 재사용 가능
    #   - 코드 중복을 줄이고 가독성을 높여줌
    #
    # def 함수이름(매개변수):
    #     함수 본문 (들여쓰기 필수!)
    #     return 반환값  (선택사항)
    #
    # 이 함수의 역할:
    #   - model_name을 받아서 해당 모델에게 질문을 보냄
    #   - 소요 시간(Latency)을 측정해서 반환
    
    # -------------------------------------------------------------------------
    # [3-1] 요청 데이터(Payload) 생성
    # -------------------------------------------------------------------------
    payload = {
        "model": model_name,
        # 전달받은 model_name 변수를 사용
        # 함수가 호출될 때마다 다른 모델 이름이 들어옴
        
        "prompt": "Hello, who are you? Answer in one short sentence.",
        # 간단한 영어 질문 (모든 모델이 잘 이해하는 언어)
        # 짧은 질문 = 순수 모델 로딩 시간을 더 명확히 볼 수 있음
        
        "stream": False
        # 스트리밍 비활성화 (한 번에 응답 받기)
    }
    
    # -------------------------------------------------------------------------
    # [3-2] 요청 시작 알림 및 시간 측정 시작
    # -------------------------------------------------------------------------
    print(f"\n[Request] Calling model: {model_name}...")
    # \n = 줄바꿈 (출력을 보기 좋게)
    # f-string으로 model_name 변수 값을 문자열에 삽입
    
    start_time = time.time()
    # 현재 시각을 기록 (시작 시점)
    
    # -------------------------------------------------------------------------
    # [3-3] API 호출 및 응답 처리
    # -------------------------------------------------------------------------
    try:
        # try-except: 에러 발생 시 프로그램이 멈추지 않도록 보호
        
        response = requests.post(url, json=payload)
        # Ollama 서버에 POST 요청 전송
        # 이 라인에서 실제로 모델이 답변을 생성하기까지 기다림
        
        elapsed = time.time() - start_time
        # 소요 시간 = 현재 시각 - 시작 시각
        
        # ---------------------------------------------------------------------
        # [3-4] 응답 상태 코드 확인
        # ---------------------------------------------------------------------
        if response.status_code == 200:
            # HTTP 상태 코드:
            #   200 = 성공 (OK)
            #   404 = 찾을 수 없음 (Not Found)
            #   500 = 서버 에러 (Internal Server Error)
            
            result = response.json()
            # JSON 응답을 파이썬 딕셔너리로 변환
            
            print(f"[Response] {result['response'].strip()}")
            # result['response'] = AI가 생성한 답변 텍스트
            # .strip() = 앞뒤 공백/줄바꿈 제거 (깔끔하게 출력)
            
            print(f"[Latency] {elapsed:.2f} seconds")
            # 소요 시간을 소수점 2자리까지 출력
            
            return elapsed
            # 📌 return: 함수를 호출한 곳으로 값을 돌려보냄
            # 이 값은 나중에 저장하거나 비교하는 데 사용 가능
            
        else:
            # 상태 코드가 200이 아니면 에러
            print(f"[Error] Status Code: {response.status_code}")
            return None
            # None = "값이 없음"을 나타내는 특별한 값
            
    except Exception as e:
        # 네트워크 오류 등 예외 발생 시
        print(f"[Exception] {e}")
        return None


# =============================================================================
# [4단계] 메인 실행 코드
# =============================================================================
# 여기서부터는 함수 정의가 아닌, 실제로 "실행되는" 코드
# 파이썬은 들여쓰기가 없는 코드를 위에서 아래로 순서대로 실행함

print("=== Ollama Automatic Model Swapping Test ===")
print("Note: If a model is not in VRAM, it will take longer to load (high latency).")
# 테스트 시작 안내 메시지


# -----------------------------------------------------------------------------
# [4-1] 1라운드: 순차적으로 각 모델 호출
# -----------------------------------------------------------------------------

print("\n--- Round 1: Sequential Loading ---")
# 라운드 1 시작 알림

for model in models:
    # 📌 for 변수 in 리스트:
    #    리스트의 각 요소를 하나씩 꺼내서 변수에 넣고 반복
    #
    # 실행 순서:
    #   1회차: model = "qwen2.5:14b"    → test_model_swap("qwen2.5:14b")
    #   2회차: model = "deepseek-r1:14b" → test_model_swap("deepseek-r1:14b")
    #   3회차: model = "gemma2:9b"       → test_model_swap("gemma2:9b")
    
    test_model_swap(model)
    # 위에서 정의한 함수 호출
    # model 변수에 담긴 모델 이름을 인자(argument)로 전달
    #
    # 💡 예상 결과:
    #    각 호출마다 이전 모델을 메모리에서 내리고 새 모델을 올림
    #    → 모든 호출에서 높은 지연 시간 (10~30초)


# -----------------------------------------------------------------------------
# [4-2] 2라운드: 다시 한 번 순차 호출
# -----------------------------------------------------------------------------

print("\n--- Round 2: Reuse/Re-loading ---")
# 라운드 2 시작 알림

for model in models:
    test_model_swap(model)
    # 동일한 순서로 다시 호출
    #
    # 💡 예상 결과 (메모리가 충분할 경우):
    #    마지막 모델(gemma2:9b)이 아직 메모리에 있다면
    #    → 첫 호출(gemma)은 빠르고, 나머지는 재로딩 필요
    #
    # 💡 예상 결과 (메모리가 부족할 경우):
    #    모든 호출에서 다시 로딩 필요
    #    → 1라운드와 비슷한 지연 시간


print("\n=== Test Finished ===")

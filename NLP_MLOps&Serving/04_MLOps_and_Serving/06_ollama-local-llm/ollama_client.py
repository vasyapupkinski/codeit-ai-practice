# -----------------------------------------------------------------------------
# [1단계] 라이브러리 불러오기 (import)
# -----------------------------------------------------------------------------
# 라이브러리 = 누군가가 미리 만들어둔 유용한 기능 모음집
# 파이썬에서는 import 키워드로 가져와서 사용함

import requests  
# requests 라이브러리: 인터넷 요청을 보내고 받는 기능
# - 예: 웹사이트에 접속하거나, API 서버에 데이터를 보내는 것
# - 설치 방법: pip install requests (이미 설치되어 있을 가능성 높음)

import time      
# time 라이브러리: 시간과 관련된 기능
# - time.time() = 현재 시각을 숫자(초 단위)로 반환
# - 코드 실행 시간을 측정할 때 주로 사용
# - 파이썬 기본 내장 라이브러리라서 따로 설치 필요 없음


# -----------------------------------------------------------------------------
# [2단계] Ollama 서버 주소 설정
# -----------------------------------------------------------------------------

url = "http://localhost:11434/api/generate"
# 변수 url에 Ollama 서버 주소를 저장
#
# 🔍 주소 분석:
# ┌────────────────┬────────────────────────────────────────────────────────────┐
# │ http://        │ 통신 방식 (웹에서 가장 흔한 프로토콜)                       │
# │ localhost      │ "내 컴퓨터"를 가리키는 특별한 이름 (= 127.0.0.1)           │
# │ :11434         │ 포트 번호 (Ollama가 기본으로 사용하는 문 번호)              │
# │ /api/generate  │ API 경로 (Ollama에게 "텍스트 생성해줘"라고 요청하는 곳)    │
# └────────────────┴────────────────────────────────────────────────────────────┘
#
# 💡 비유: 
#    - localhost:11434 = 우리 집 주소
#    - /api/generate = 그 집 안의 "AI 응답 담당 부서"


# -----------------------------------------------------------------------------
# [3단계] 요청 데이터(Payload) 만들기
# -----------------------------------------------------------------------------

payload = {
    "model": "gemma2:9b",   
    # 어떤 모델을 사용할지 지정
    # - 현재 WSL에 설치된 모델 이름과 정확히 일치해야 함
    # - ollama list 명령어로 설치된 모델 확인 가능
    # - 예: "llama3", "gemma2:9b", "qwen2.5:14b" 등
    
    "prompt": "AI 모델에서 양자화가 뭔지 3줄로 설명해줘.",
    # AI에게 보낼 질문(프롬프트)
    # - 이 문자열이 그대로 AI에게 전달됨
    # - AI는 이 질문을 읽고 답변을 생성함
    
    "stream": False
    # 스트리밍 모드 설정
    # - False: 답변이 다 완성될 때까지 기다렸다가 한 번에 받음
    # - True: 글자가 생성될 때마다 실시간으로 조금씩 받음 (챗GPT처럼)
    # 
    # 💡 False를 쓰는 이유: 
    #    코드에서 전체 답변을 한꺼번에 처리하기 더 편함
}
# 📌 payload는 파이썬 딕셔너리(dict) 형태
# - 딕셔너리 = 키(key)와 값(value)의 쌍으로 이루어진 데이터 구조
# - JSON 형식과 거의 동일해서 API 통신에 자주 사용됨


# -----------------------------------------------------------------------------
# [4단계] 에러 처리를 위한 try-except 블록
# -----------------------------------------------------------------------------

try:
    # try 블록: "이 안의 코드를 실행해봐. 문제가 생기면 except로 가"
    # - 네트워크 오류, 서버 다운 등 예상치 못한 문제에 대비
    
    # -------------------------------------------------------------------------
    # [4-1] 시간 측정 시작
    # -------------------------------------------------------------------------
    start = time.time()
    # time.time() = 1970년 1월 1일부터 지금까지 흐른 초(seconds)를 반환
    # 예: 1706234567.123456 같은 숫자
    # 이 숫자를 start 변수에 저장해둠
    
    # -------------------------------------------------------------------------
    # [4-2] Ollama 서버에 POST 요청 보내기
    # -------------------------------------------------------------------------
    res = requests.post(url, json=payload)
    # requests.post(주소, json=데이터)
    # - POST 방식으로 url 주소에 payload 데이터를 보냄
    # - GET vs POST:
    #   - GET: 정보를 "가져올 때" (예: 웹페이지 열기)
    #   - POST: 정보를 "보낼 때" (예: 로그인, AI에게 질문)
    # - json=payload: 딕셔너리를 자동으로 JSON 형식으로 변환해서 보냄
    # - 응답(response)이 돌아오면 res 변수에 저장됨
    #
    # 🔄 이 한 줄이 실행되는 동안:
    #    1. 우리 코드 → Ollama 서버로 요청 전송
    #    2. Ollama 서버가 gemma2:9b 모델에게 질문 전달
    #    3. 모델이 답변 생성 (이 과정이 가장 오래 걸림)
    #    4. Ollama 서버 → 우리 코드로 응답 반환
    
    # -------------------------------------------------------------------------
    # [4-3] 시간 측정 종료 및 계산
    # -------------------------------------------------------------------------
    elapsed = time.time() - start
    # 현재 시각 - 시작 시각 = 걸린 시간(초)
    # 예: 1706234572.5 - 1706234567.1 = 5.4초
    
    # -------------------------------------------------------------------------
    # [4-4] 응답 데이터 파싱(분석)
    # -------------------------------------------------------------------------
    data = res.json()
    # res.json() = 서버 응답을 JSON에서 파이썬 딕셔너리로 변환
    # 
    # Ollama가 보내주는 응답 예시:
    # {
    #     "model": "gemma2:9b",
    #     "response": "양자화는 ... (AI의 답변)",
    #     "done": true,
    #     "context": [1234, 5678, ...],
    #     ...
    # }
    
    # -------------------------------------------------------------------------
    # [4-5] 에러 확인 및 결과 출력
    # -------------------------------------------------------------------------
    if "error" in data:
        # 만약 응답 데이터 안에 "error"라는 키가 있다면
        # → Ollama에서 뭔가 문제가 생겼다는 뜻 (예: 모델이 없음)
        print(f"!!! Error from Ollama: {data['error']}")
        # f-string: 문자열 안에 {변수}를 넣어서 값을 출력하는 방법
    else:
        # 에러가 없으면 정상적으로 답변 출력
        print("=== Model Response ===")
        print(data["response"])
        # data["response"] = AI가 생성한 답변 텍스트
        
        print("\n=== Latency ===")  # \n = 줄바꿈
        print(f"{elapsed:.2f}s")
        # {elapsed:.2f} = elapsed 값을 소수점 2자리까지 표시
        # 예: 5.4321 → "5.43s"

except Exception as e:
    # except 블록: try 안에서 에러가 발생하면 여기로 옴
    # Exception = 모든 종류의 에러를 잡겠다는 뜻
    # as e = 에러 정보를 e 변수에 저장
    print(f"!!! Connection Error: {e}")
    # 예: 서버가 꺼져있으면 "Connection refused" 같은 메시지가 나옴


